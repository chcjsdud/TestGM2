// 이게 전역 오퍼레이팅 입니다.
// 단항은 할수가 없을 겁니다.
// 내가 만든 사용자 정의 자료형이 우항에 와야할 상황이 오면 만드는게
// 전역 오퍼레이팅
int operator-(const int Value, MyInt _NewInt)
{
    return 100;
}

