#include <iostream>

class MyInt
{
public:
    // 맴버 오퍼레이터의 항은 우항에 상대가 들어와줘야 한다.
    MyInt operator-(const int Value)
    {
        // 좌항이 자기 자신이야잖아
        // this
        return MyInt();
    }

    int operator[](int _Value)
    {
        return 0;
    }
};

